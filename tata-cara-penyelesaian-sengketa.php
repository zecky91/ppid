                        <h3 class="post__title entry-title">TATA CARA PENGAJUAN PERMOHONAN PENYELESAIAN SENGKETA KE KIP</h3>

                    </div>
                    <p><strong>1. SENGKETA INFORMASI</strong></p>
                    <p style="text-align: justify;">Sengketa Informasi Publik adalah sengketa yang terjadi antara Badan Publik dan Pemohon Informasi Publik berdasarkan alasan sebagaimana dimaksud dalam Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik</p>
                    <p><strong>2. PROSEDUR PENYELESAIAN SENGKETA INFORMASI KEPADA KOMISI INFORMASI PUSAT</strong></p>
                    <ul>
                        <li style="text-align: justify;">Pemohon informasi yang memiliki keberatan terhadap jawaban yg diberikan oleh PPID dapat mengajukan sengketa informasi ke KIP</li>
                        <li style="text-align: justify;">Pengajuan Sengketa Informasi Publik ke Komisi Informasi diajukan dalam waktu 14 hari kerja setelah diterimanya tanggapan tertulis dari Atasan PPID yang dianggap tidak memuaskan Pemohon Informasi Publik</li>
                        <li style="text-align: justify;">Komisi Informasi Pusat harus mulai mengupayakan penyelesaian Sengketa Informasi Publik melalui Mediasi dan/atau Ajudikasi paling lambat 14 hari kerja setelah menerima permohonan penyelesaian Sengketa Informasi Publik. Proses penyelesaian sengketa paling lambat dapat diselesaikan dalam waktu 100 (seratus) hari kerja</li>
                        <li style="text-align: justify;">Jika pada tahap mediasi dihasilkan kesepakatan, maka hasil kesepakatan mediasi tersebut ditetapkan oleh Putusan Komisi Informasi</li>
                        <li style="text-align: justify;">Jika pada tahap mediasi tidak dihasilkan kesepakatan atau terjadi penarikan diri dari salah satu pihak atau para pihak, maka Komisi Informasi melanjutkan proses penyelesaian sengketa melalui adjudikasi</li>
                        <li style="text-align: justify;">Jika Pemohon Informasi puas atas keputusan Adjudikasi Komisi Informasi, sengketa dinyatakan selesai</li>
                        <li style="text-align: justify;">Jika pemohon Informasi tidak menerima/tidak puas dengan Putusan Komisi Informasi, maka dapat mengajukan gugatan ke pengadilan dalam waktu 14 hari kerja sejak diterimanya putusan tersebut, dan menyatakan secara tertulis bahwa tidak menerima/ tidak puas dengan Putusan Adjudikasi Komisi Informasi</li>
                    </ul>
