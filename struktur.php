                        <h3 class="post__title entry-title">Struktur Organisasi PPID</h3>

                    </div>
                    <img src="img/struktur.jpg" alt="image">
                    <p>Struktur PPID (Pusat Informasi dan Dokumentasi) di SMK Negeri 1 Bukittinggi dapat bervariasi tergantung pada ukuran sekolah, kompleksitas operasi, dan kebutuhan spesifik. Namun, berikut adalah contoh struktur umum PPID di sebuah sekolah:</br>



                    <p><strong>Kepala Sekolah:</strong></br>
                        Sebagai pemimpin sekolah, Kepala Sekolah memiliki tanggung jawab akhir dalam hal keterbukaan informasi publik dan memastikan bahwa PPID berfungsi sesuai dengan ketentuan yang berlaku.


                    <p><strong>Kepala PPID:</strong></br>
                        Kepala PPID adalah individu yang bertanggung jawab langsung atas operasi dan pengelolaan PPID di SMK Negeri 1 Bukittinggi.


                    <p><strong>Petugas PPID:</strong></br>
                        Petugas PPID adalah staf yang bekerja di bawah pengawasan Kepala PPID. Mereka berperan dalam menerima, memproses, dan mengelola permohonan informasi dari masyarakat serta menjaga dan mengatur dokumentasi.


                    <p><strong>Sekretaris PPID:</strong></br>
                        Sekretaris PPID dapat membantu dalam administrasi dan dokumentasi terkait dengan permohonan informasi, pengarsipan, dan pelaporan.


                    <p><strong>Tim Pengelola Arsip:</strong></br>
                        Tim ini bertanggung jawab atas pengelolaan arsip dan dokumentasi sekolah, termasuk pengindeksan, pemeliharaan, dan pemusnahan sesuai ketentuan.


                    <p><strong>Tim IT (Teknologi Informasi):</strong></br>
                        Tim IT dapat membantu dalam mengembangkan dan menjaga sistem informasi yang mendukung operasi PPID, seperti basis data dan situs web.


                    <p><strong>Staf Administrasi:</strong></br>
                        Staf administrasi dapat membantu dalam proses administratif terkait permohonan informasi, komunikasi dengan pemohon, dan administrasi umum PPID.


                    <p><strong>Pemohon Informasi Publik:</strong></br>
                        Masyarakat umum yang mengajukan permohonan informasi publik kepada PPID juga merupakan bagian dari struktur, karena mereka adalah pihak yang memicu proses permohonan informasi.



                    <p>Struktur ini dapat disesuaikan sesuai dengan kebutuhan dan peraturan yang berlaku di SMK Negeri 1 Bukittinggi. Tujuan utama dari struktur PPID adalah memastikan bahwa keterbukaan informasi publik terpenuhi dengan baik dan memberikan layanan yang transparan kepada masyarakat.</br>

