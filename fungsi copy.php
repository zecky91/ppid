                        <h3 class="post__title entry-title">TUGAS & FUNGSI</h3>

                    </div>

                    <p>Tugas dan fungsi PPID (Pusat Informasi dan Dokumentasi) SMK Negeri 1 Bukittinggi adalah untuk mengelola informasi publik dan memfasilitasi akses masyarakat kepada informasi tersebut sesuai dengan ketentuan Undang-Undang Keterbukaan Informasi Publik (UU KIP). Berikut adalah beberapa tugas dan fungsi PPID SMK Negeri 1 Bukittinggi:</p>

                    <ol>
                        <li>
                            <p><strong>Mengelola Informasi Publik:</strong> PPID bertanggung jawab untuk mengumpulkan, mengelola, dan menjaga informasi-informasi publik yang dimiliki oleh SMK Negeri 1 Bukittinggi. Ini mencakup dokumen-dokumen, data, laporan, dan informasi lain yang relevan.</p>
                        </li>
                        <li>
                            <p><strong>Memberikan Akses Informasi:</strong> Salah satu fungsi utama PPID adalah memastikan bahwa informasi publik tersebut dapat diakses oleh masyarakat dengan mudah. Mereka harus memberikan akses yang transparan, cepat, dan efisien kepada pemohon yang meminta informasi.</p>
                        </li>
                        <li>
                            <p><strong>Memberikan Informasi Kepada Masyarakat:</strong> PPID harus memberikan informasi kepada masyarakat tentang jenis informasi yang mereka miliki, cara mengajukan permohonan informasi, dan prosedur yang harus diikuti untuk mendapatkan informasi tersebut.</p>
                        </li>
                        <li>
                            <p><strong>Mengelola Permohonan Informasi:</strong> PPID harus menerima, memproses, dan menjawab permohonan informasi yang diajukan oleh masyarakat sesuai dengan ketentuan UU KIP. Mereka juga harus memberikan informasi tersebut sesuai dengan ketentuan waktu yang ditetapkan oleh undang-undang.</p>
                        </li>
                        <li>
                            <p><strong>Melindungi Informasi Rahasia:</strong> PPID harus menjaga kerahasiaan informasi yang dikecualikan dari akses publik sesuai dengan undang-undang, seperti informasi yang berkaitan dengan keamanan nasional atau informasi yang bersifat pribadi.</p>
                        </li>
                        <li>
                            <p><strong>Pengembangan Sistem Informasi:</strong> PPID mungkin bertanggung jawab untuk mengembangkan sistem informasi yang memudahkan pengelolaan dan pencarian informasi, sehingga proses akses informasi menjadi lebih efisien.</p>
                        </li>
                        <li>
                            <p><strong>Melakukan Pelatihan dan Sosialisasi:</strong> PPID dapat melaksanakan pelatihan dan sosialisasi kepada staf dan masyarakat tentang pentingnya keterbukaan informasi publik dan hak-hak masyarakat untuk mengakses informasi tersebut.</p>
                        </li>
                        <li>
                            <p><strong>Melakukan Evaluasi dan Pelaporan:</strong> PPID mungkin perlu melakukan evaluasi berkala terhadap kinerja mereka dalam memenuhi kewajiban-kewajiban yang berkaitan dengan keterbukaan informasi publik. Mereka juga dapat membuat laporan tahunan tentang pelaksanaan UU KIP di lingkungan SMK Negeri 1 Bukittinggi.</p>
                        </li>
                    </ol>

                    <p>Tugas dan fungsi PPID ini penting dalam menjaga prinsip keterbukaan informasi dan hak masyarakat untuk mengakses informasi publik yang relevan dengan lembaga tersebut.</p>


